package com.edu.htmlparse;

public class HeadVo {

	String vpmm;
	
	String level;
	
	String projects;
	
	String plcs;
	
	int size = 4;

	public String getVpmm() {
		return vpmm;
	}

	public void setVpmm(String vpmm) {
		this.vpmm = vpmm;
	}

	public String getLevel() {
		return level;
	}

	public void setLevel(String level) {
		this.level = level;
	}

	public String getProjects() {
		return projects;
	}

	public void setProjects(String projects) {
		this.projects = projects;
	}

	public String getPlcs() {
		return plcs;
	}

	public void setPlcs(String plcs) {
		this.plcs = plcs;
	}

	public int getSize() {
		return size;
	}

	public void setSize(int size) {
		this.size = size;
	}

	
}
